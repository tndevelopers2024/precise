    <script>
        document.addEventListener('DOMContentLoaded', function () {
            var sliderWrap = document.getElementById('customTestiSlider');
            if (sliderWrap) {
                var slides = sliderWrap.querySelectorAll('.custom-slide');
                var dots = sliderWrap.querySelectorAll('.t-dot');
                var currentIndex = 0;
                var slideTimer;

                function goToSlide(index) {
                    slides.forEach(function(s) { s.classList.remove('active'); });
                    dots.forEach(function(d) { d.classList.remove('active'); });
                    
                    currentIndex = index;
                    if (currentIndex >= slides.length) currentIndex = 0;
                    if (currentIndex < 0) currentIndex = slides.length - 1;
                    
                    slides[currentIndex].classList.add('active');
                    dots[currentIndex].classList.add('active');
                }

                function nextSlide() {
                    goToSlide(currentIndex + 1);
                }

                function startTimer() {
                    slideTimer = setInterval(nextSlide, 4500); // 4.5s auto loop
                }

                dots.forEach(function(dot) {
                    dot.addEventListener('click', function() {
                        clearInterval(slideTimer); // pause auto when clicked
                        var idx = parseInt(this.getAttribute('data-slide-index'));
                        goToSlide(idx);
                        startTimer(); // restart auto 
                    });
                });

                startTimer(); // initialize
            }
        });
    </script>

