    // Bulletproof FAQ Accordion functionality
    $(document).ready(function() {
        // Prevent Bootstrap's native collapse from clashing if versions mismatch
        $('.custom-faq-accordion .accordion-button').removeAttr('data-bs-toggle data-toggle data-target data-bs-target');

        // Hide all initially collapsed items securely
        $('.custom-faq-accordion .accordion-collapse:not(.show)').hide();

        $('.custom-faq-accordion .accordion-button').on('click', function(e) {
            e.preventDefault();
            
            var $btn = $(this);
            var $item = $btn.closest('.accordion-item');
            var $body = $item.find('.accordion-collapse');
            var $parent = $btn.closest('.custom-faq-accordion');
            
            // Check if is already open
            var isClosed = $btn.hasClass('collapsed');
            
            // Collapse all opened items in this parent column
            $parent.find('.accordion-button').addClass('collapsed').attr('aria-expanded', 'false');
            $parent.find('.accordion-item').removeClass('active-faq');
            $parent.find('.accordion-collapse').slideUp(250);
            
            // If the clicked one was closed, open it smoothly
            if (isClosed) {
                $btn.removeClass('collapsed').attr('aria-expanded', 'true');
                $item.addClass('active-faq');
                $body.slideDown(250);
            }
        });
    });
    </script>

