                        // Get target pane ID
                        const targetId = this.getAttribute('data-target');

                        // Hide all panes
                        tabPanes.forEach(pane => {
                            pane.classList.remove('active');
                        });

                        // Show targeted pane
                        const targetPane = document.getElementById(targetId);
                        if (targetPane) {
                            targetPane.classList.add('active');
                        }
                    });
                });
